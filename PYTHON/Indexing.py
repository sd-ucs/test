# INDEXING IN LOOP
x = [1, 2, 3, 4, 5, 6, 7]
print ("FIRST ELEMENT", x[0])
print ("SECOND ELEMENT", x[1])
print ("LAST ELEMENT", x[-1])
print ("SECOND LAST ELEMENT", x[-2])

# INDEXING IN TUPLE
y = (1, 2, 3, 4, 5)
print ("FIRST ELEMENT", y[0])
print ("SECOND ELEMENT", y[1])
print ("LAST ELEMENT", y[-1])
print ("SECOND LAST ELEMENT", y[-2])

# INDEXING IN STRING
z = 'SerpentCS'
print ("FIRST CHARACTER", z[0])
print ("SECOND CHARACTER", z[1])
print ("LAST CHAR", z[-1])
print ("SECOND LAST CHARACTER", z[-2])